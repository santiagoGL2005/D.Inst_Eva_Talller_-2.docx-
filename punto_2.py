# Variables
a = 7
b = 3

# Comparaciones
resultado_a = a != b
resultado_b = a == b
resultado_c = a > b
resultado_d = (a + b) < 2
resultado_e = (a > 7) and (b == 3)
resultado_f = (a < 7) or (b == 3)
resultado_g = (a < 7) or (b != 3)

# Mostrar resultados
print(f"a != b: {resultado_a}")
print(f"a == b: {resultado_b}")
print(f"a > b: {resultado_c}")
print(f"(a + b) < 2: {resultado_d}")
print(f"(a > 7) Y (b == 3): {resultado_e}")
print(f"(a < 7) O (b == 3): {resultado_f}")
print(f"(a < 7) O (b != 3): {resultado_g}")