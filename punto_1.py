# Operaciones
resultado_a = (5 + 5) * 5
resultado_b = 5 + (5 * 5)
resultado_c = 5 % 2
resultado_d = (5 - (6 / 3)) * 2

# Mostrar resultados
print(f"a. (5+5)*5 = {resultado_a}")
print(f"b. (5+(5*5) = {resultado_b}")
print(f"c. 5 modulo 2 = {resultado_c}")
print(f"d. (5-(6/3))*2 = {resultado_d}")
